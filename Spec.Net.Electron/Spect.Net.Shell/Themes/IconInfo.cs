namespace Spect.Net.Shell.Themes
{
    /// <summary>
    /// This struct represents information about an icon in the registry.
    /// </summary>
    public struct IconInfo
    {
        /// <summary>
        /// SVG path string.
        /// </summary>
        public string Path;

        /// <summary>
        /// Icon width.
        /// </summary>
        public int Width;

        /// <summary>
        /// Icon height.
        /// </summary>
        public int Height;

        /// <summary>
        /// Optional fill value.
        /// </summary>
        public string Fill;
    }
}
